public class TiposPrimitivos {
    public static void main(String[] args) throws Exception {
        System.out.println("Tipos Primitivos");

        // Primitivos Numerico - Inteiros
        byte b = 10; // 1 byte -> 0x 0d
        short s = 20; // 2bytes
        int i = 10; // 4 bytes
        long l = 40; // 8 bytes

        // Primitivos Numerico - Flutuante
        float f = 1.0f; // 4 bytes
        double d = 2.0; // 8

        // Primitivos Booleano
        char c = 'a'; // 2 bytes
        boolean bool = true; // 1 byte

    }
}
